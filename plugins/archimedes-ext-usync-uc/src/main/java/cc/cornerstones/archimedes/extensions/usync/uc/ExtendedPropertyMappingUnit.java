package cc.cornerstones.archimedes.extensions.usync.uc;

public class ExtendedPropertyMappingUnit {
    private String name;
    private Long targetExtendedPropertyUid;
    private String targetExtendedPropertyName;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Long getTargetExtendedPropertyUid() {
        return targetExtendedPropertyUid;
    }

    public void setTargetExtendedPropertyUid(Long targetExtendedPropertyUid) {
        this.targetExtendedPropertyUid = targetExtendedPropertyUid;
    }

    public String getTargetExtendedPropertyName() {
        return targetExtendedPropertyName;
    }

    public void setTargetExtendedPropertyName(String targetExtendedPropertyName) {
        this.targetExtendedPropertyName = targetExtendedPropertyName;
    }
}
